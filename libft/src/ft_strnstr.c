#include "../include/libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	int i;
	int j;

	i = 0;
	if (*little == '\0')
		return ((char *)big);
	while (big[i] != '\0' && len-- > 0)
	{
		j = 0;
		while (big[i + j] == little[j] && j <= (int)len)
		{
			if (little[j + 1] == '\0')
				return ((char *)(big + i));
			j++;
		}
		i++;
	}
	return (NULL);
}
