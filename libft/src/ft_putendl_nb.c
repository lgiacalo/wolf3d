#include "../include/libft.h"

int	ft_putendl_nb(char const *s)
{
	size_t	i;

	if (!s)
		return (-1);
	i = ft_strlen(s);
	write(1, s, i);
	write(1, "\n", 1);
	return (i + 1);
}
