#include "../include/libft.h"

void	ft_arraydel(char ***s)
{
	int	i;

	i = 0;
	while (*s && (*s)[i] != 0)
	{
		ft_strdel(&((*s)[i]));
		i++;
	}
	free(*s);
	*s = NULL;
}
